"""
This script contains the function for the sum of two numbers.
"""


def f(a: int, b: int) -> int:
    """
    This function sums two numbers.

    Parameters
    ----------
    a : First number.
    b : Second number.

    Returns
    -------
    Sum of the numbers.
    """

    return a + b


if __name__ == "__main__":  # pragma: no cover
    f(1, 2)
